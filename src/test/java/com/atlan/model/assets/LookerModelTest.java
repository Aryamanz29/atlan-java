    @Test(
            groups = {"LookerModel.deserialize"},
            dependsOnGroups = {"LookerModel.serialize"})
    void deserialization() throws JsonProcessingException {
        assertNotNull(serialized);
        frodo = Serde.mapper.readValue(serialized, LookerModel.class);
        assertNotNull(frodo);
    }

    @Test(
            groups = {"LookerModel.equivalency"},
            dependsOnGroups = {"LookerModel.serialize", "LookerModel.deserialize"})
    void serializedEquivalency() {
        assertNotNull(serialized);
        assertNotNull(frodo);
        String backAgain = frodo.toJson();
        assertEquals(backAgain, serialized, "Serialization is not equivalent after serde loop,");
    }

